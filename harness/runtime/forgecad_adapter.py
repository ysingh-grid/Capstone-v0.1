"""
harness/runtime/forgecad_adapter.py
====================================
ForgeCAD handoff adapter — real .forge.js emitter.

PRD §6.2: ``forgecad_emit`` generates editable ForgeCAD model code and
attaches previews, exports, and review notes.

This adapter emits a valid **ForgeCAD project** that engineers open with:
    npm install -g forgecad   (one-time setup)
    forgecad studio <project_dir>

The project contains:
    forge.config.json           — ForgeCAD project manifest
    models/<name>.forge.js      — parametric JavaScript model (live sliders)

Architecture note:
    CadQuery Python remains the OCCT-verified geometry authority used by the
    harness for volume, bounding-box, and watertightness checks.  The .forge.js
    file is generated in parallel by a dedicated LLM coder agent and is the
    engineer-facing editable surface.  Both artifacts are stored; the .forge.js
    is what the engineer opens in the ForgeCAD Studio workbench.
"""

from __future__ import annotations

import json
import os
import sys
from pathlib import Path
from typing import Optional

# Ensure CADSmith agents are importable from here
_PROJECT_ROOT = Path(__file__).resolve().parents[2]
_CADSMITH_DIR = _PROJECT_ROOT / "CADSmith"
if str(_CADSMITH_DIR) not in sys.path:
    sys.path.insert(0, str(_CADSMITH_DIR))

from autofab import agents  # noqa: E402

from harness.schema.primitives import PrimitivePlan


